// DEFAULT LANGUAGE DETECTION => BROWSER SETTINGS
const availableLanguages = ['en', 'fr'];
let currentLang = 'en';
if (typeof window !== 'undefined') {
	currentLang = navigator.language.slice(0, 2) || 'en';
	window.currentLang = currentLang;
	translatePage();
}

// TEXT LANGUAGE TRANSLATION FUNCTIONS
function getLanguage() {
	if (typeof window === 'undefined') return 'en';
	return window.currentLang || currentLang;
}
function translatePage() {
	// TRANSLATE ALL ELEMENTS WITH data-l ATTRIBUTE
	for (const el of document.querySelectorAll('[data-l]'))
		el.textContent = text(el.dataset.l);

	// SET CSS CLASS ON BODY FOR LANGUAGE-SPECIFIC STYLES
	for (const l of availableLanguages)
		document.body.classList.remove(l);
	document.body.classList.add(getLanguage());
}
export function setLanguage(lang) {
	if (!availableLanguages.includes(lang)) return;
	currentLang = lang;
	translatePage();
}
export function text(key) {
	return TEXT_TRANSLATIONS[key]?.[getLanguage()] || TEXT_TRANSLATIONS[key]?.['en'] || '';
}

